Switch Case

void main() {
  
 String grade='A';
  
  switch (grade){
    case 'A':
      print("Excellent");
      break;
      
    case 'B':
      print("Better");
      break;
      
    case 'C':
      print("Good");
      break;
      
      default :
      print("Failed");
      break;
        
  }
  
  
 }